                                          <h3>Why KASU Library?</h3>
                                          <h5>KASU Library Internship Program</h5>
                                          <p>
                                                  The Objective of the KASU Library Internship program is to provide challenging opportunities for talented undergraduate and graduate students from local & international institutions to gain practical insights into the operational environment of Kasu Library as a service institute.
                                          </p>
                                          <p>
                                                  The Program is designed to provide an enriching experience characterised by measurable on-the-job training, designed to help you succeed in your division. It is an avenue to develop leadership traits in young individuals and assist them in realising their potentials. You'll work on client projects, develop technical skills and experience our vibrant culture. We’ll ensure you have adequate responsibilities, opportunities and support to push yourself. At KASU Library, we have an inclusive culture to ensure you feel welcomed and valued. All interns are guaranteed close interactions with industry experts, learning a great deal about the institute and how we do business.
                                          </p>        
                                                  We aspire to build a long term relationship between Infosol and the interns for potential full-time employment in the future.
                                          <p>        
                                                  Our Interns are assigned to our Practice and non- Practice divisions based on their areas of interest and availability of these units. They work with our seasoned experienced professionals across various units and divisions.
                                          </p>
                                      </div>
                                      <div role="tabpanel" class="tab-pane fade" id="category">
                                          <h3> Undergraduate Internship</h3>

                                          The Undergraduate Internship program is for candidates currently pursuing a university degree. The prospective candidate must have completed at least the second year of university, but must NOT be in final year. We invite candidates from IT, Business & Legal courses with an outstanding interim academic record and an interest in the firms service offerings, across all divisions. Competitive and attractive compensation is rewarded upon completion.
                                          
                                          <h3> Graduate Internship</h3>
                                          
                                          This program is open to postgraduate level students across IT & Business disciplines, seeking work experience in the interim. We are looking for students within the 1st class or 2.1 degree class with an interest in the firms service offerings, across all divisions. On this program, you’ll join industry experts developing meaningful solutions for clients, giving you a great head start on your career. Competitive and attractive compensation is rewarded upon completion.
            
                                          <h3> Pre-NYSC Internship </h3>
                                          
                                          This program is aimed at graduate level students, seeking an interim job, whilst awaiting NYSC deployment. We invite candidates from IT & Business disciplines, however, this program is exclusive to our Management Consulting Unit. It provides a mutual and extensive opportunity to assess candidates capability and cultural fit into the Kasu Library way as it may eventually be the standard entry path into a more permanent Analyst position within the firms Management Consulting Unit. Competitive and attractive compensation is rewarded upon completion
                                      </div>
